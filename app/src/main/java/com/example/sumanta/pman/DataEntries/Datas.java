public class Datas {
    private String dataName;
    private int dataId;

    public Datas(String dataName, int dataId) {
        this.dataName = dataName;
        this.dataId = dataId;
    }

    public String getDataName() {
        return dataName;
    }

    public int getDataId() {
        return dataId;
    }
}
